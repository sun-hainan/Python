def quick_sort(arr):
    """
    快速排序 - Quick Sort
    
    原理:
    1. 选择一个基准元素(pivot)
    2. 将数组分为两部分: <= pivot 和 > pivot
    3. 递归排序左右两部分
    4. 合并结果
    
    参数:
        arr: 可比较元素组成的列表
    
    返回:
        排序后的新列表
    
    时间复杂度: O(n log n) 平均, O(n²) 最坏
    空间复杂度: O(n)
    """
    # 递归终止条件
    if len(arr) <= 1:
        return arr
    
    # 选择基准(中间位置)
    mid = len(arr) // 2
    pivot = arr[mid]
    
    # 分组
    less = []
    greater = []
    
    # 遍历并分类
    for item in arr:
        if item == arr[mid]:
            continue
        if item <= pivot:
            less.append(item)
        else:
            greater.append(item)
    
    # 递归处理左右两部分
    # 公式: left_sorted + pivot + right_sorted
    return quick_sort(less) + [pivot] + quick_sort(greater)


