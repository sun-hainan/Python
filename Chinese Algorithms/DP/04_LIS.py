"""
LIS - 最长递增子序列
==========================================

【问题定义】
在一个序列中找出最长的递增子序列。
子序列可以不连续，但必须保持递增顺序。

【时间复杂度】O(n^2)
【空间复杂度】O(n)

【应用场景】
- 俄罗斯套娃信封问题
- 股票最长上涨区间
- 合唱队形问题（排队身高）
- 防御工事布置

【何时使用】
- 求最长递增/递减序列
- 套娃类问题
- 序列分析
- 贪心无法解决的递增问题

【实际案例】
# 股票最长上涨期分析
# 帮投资者找出股价持续上涨的最长时间段
stock_prices = [100, 120, 105, 110, 130, 125, 140, 135, 150]
lis(stock_prices)  # 输出最长上涨区间长度

# 合唱队形（选最多人站成一排，身高递增）
heights = [170, 165, 180, 160, 175, 172]
lis(heights)  # 最多能选几个人

# 活动参与顺序安排
# 一系列活动，每个活动有开始和结束时间
# 最多能参加多少个不冲突的活动
activities = [(1,4), (3,5), (0,6), (5,7), (3,9)]
# 选最长序列
"""

def lis(arr):
    """
    最长递增子序列 - 动态规划解法
    
    Args:
        arr: 待处理列表
        
    Returns:
        LIS长度
    """
    if not arr:
        return 0
    
    n = len(arr)
    dp = [1] * n
    
    for i in range(1, n):
        for j in range(i):
            if arr[j] < arr[i]:
                dp[i] = max(dp[i], dp[j] + 1)
    
    return max(dp)
