"""
Insertion Sort - 插入排序
==========================================

【算法原理】
像整理扑克牌，将每个元素插入到左侧已排序部分的正确位置。

【时间复杂度】O(n^2)
【空间复杂度】O(1)
【稳定性】稳定

【应用场景】
- 扑克牌整理（模拟手牌插入过程）
- 图书馆还书后插入到合适位置
- 实时数据流排序
- 基本有序数据的增量排序

【何时使用】
- 数据基本有序
- 数据量 n < 50
- 需要持续插入新数据的场景
"""

def insertion_sort(arr):
    """
    插入排序
    """
    for i in range(1, len(arr)):
        key = arr[i]
        j = i - 1
        while j >= 0 and arr[j] > key:
            arr[j + 1] = arr[j]
            j -= 1
        arr[j + 1] = key
    return arr
