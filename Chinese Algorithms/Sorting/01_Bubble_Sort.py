"""
Bubble Sort - 冒泡排序
==========================================

【算法原理】
遍历数组，比较相邻元素，如果顺序错误就交换。
每轮遍历后，最大元素会"冒泡"到最后。

【时间复杂度】O(n^2)
【空间复杂度】O(1)
【稳定性】稳定

【应用场景】
- 教学演示排序思想
- 数据基本有序的小规模排序
- 嵌入式设备（代码简单，内存占用小）

【何时使用】
- 数据量 n < 1000
- 数据基本有序
- 需要稳定排序
- 嵌入式/硬件受限环境

【实际案例】
# 考试成绩初排序（数据量小，基本有序）
scores = [85, 92, 78, 96, 88, 73, 91]
bubble_sort(scores)  # 快速得到基本排名
"""

def bubble_sort(arr):
    """
    冒泡排序
    
    Args:
        arr: 待排序列表(原地修改)
        
    Returns:
        排序后的列表
    """
    n = len(arr)
    
    for i in range(n - 1, 0, -1):
        for j in range(i):
            if arr[j] > arr[j + 1]:
                arr[j], arr[j + 1] = arr[j + 1], arr[j]
    
    return arr
