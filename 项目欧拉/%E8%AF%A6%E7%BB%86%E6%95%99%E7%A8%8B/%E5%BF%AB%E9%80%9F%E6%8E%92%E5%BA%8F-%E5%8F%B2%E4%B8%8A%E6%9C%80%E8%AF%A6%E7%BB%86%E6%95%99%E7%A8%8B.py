# ============================================================

# 快速排序函数

# ============================================================

def 快速排序(数组):

    """

    快速排序 - 分治策略



    原理：

    1. 选择一个基准元素（pivot）

    2. 将数组分成两部分：左边 <= pivot，右边 > pivot

    3. 递归处理左右两部分

    4. 合并结果：[已排序左边] + [基准] + [已排序右边]



    参数:

        数组: 待排序的数字列表



    返回值:

        排好序的新列表

    """



    # -------- 递归终止条件 --------

    # 当数组为空或只有一个元素时，不需要排序

    # 直接返回（一个元素本身就是有序的）

    if len(数组) <= 1:

        return 数组



    # -------- 第1步：选择基准 --------

    # 选中间位置的元素作为基准

    # 这种"中间基准"策略可以避免最坏情况

    中间位置 = len(数组) // 2        # 整除，得到中间索引

    基准 = 数组[中间位置]             # 基准值



    # -------- 第2步：创建分组容器 --------

    左边 = []     # 存放小于等于基准的元素

    右边 = []     # 存放大于基准的元素



    # -------- 第3步：遍历元素，分到两边 --------

    for 数字 in 数组:



        # 跳过基准元素本身（它已经被选出来了）

        if 数字 == 数组[中间位置]:

            continue



        # 根据大小放入对应容器

        if 数字 <= 基准:

            左边.append(数字)

        else:

            右边.append(数字)



    # -------- 第4步：递归排序左右两部分 --------

    # 递归调用：对"左边"继续进行快速排序

    # 递归调用：对"右边"继续进行快速排序

    已排序的左边 = 快速排序(左边)

    已排序的右边 = 快速排序(右边)



    # -------- 第5步：合并三部分 --------

    # 格式：[已排序左边] + [基准] + [已排序右边]

    return 已排序的左边 + [基准] + 已排序的右边





